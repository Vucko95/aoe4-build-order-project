import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";


import { api } from "~/utils/api";
import { useState } from "react";

const SubmitPage: NextPage = () => {
  const createBuildMutation = api.builds.createBuild.useMutation();
  const [civilization, setCivilization] = useState("Chinese");
  const [build, setBuildOrder] = useState("");

  async function handleSubmitBuildOrder (e: React.FormEvent) {

    e.preventDefault();
    await createBuildMutation.mutateAsync({
      civilization,
      build,
    })
  }
  return (
    <>
      <Head>
        <title>Submit Build</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center gap-6 text-black dark:bg-gray-800 dark:text-white " >
        {/* <h1>Age of Empires 4 - Build Order</h1>
        <Link className="border p-2" href="/">Home</Link>
        <Link className="border p-2" href="/builds"> Builds</Link> */}

        <form className="flex flex-col gap-4" onSubmit={handleSubmitBuildOrder}>
          <select 
            value={civilization}  
            onChange={(e) => setCivilization(e.target.value)}
            id="select-civ" 
            required> 
            <option value="Chinese">Chinese</option>
            <option value="English">English</option>
            <option value="French">French</option>
          </select>
          <textarea value={build} onChange={(e) => setBuildOrder(e.target.value)} required ></textarea>
          <button className="p-5 bg-white">Submit Build</button>
        </form>
      </main>
    </>
  );
};

export default SubmitPage;


